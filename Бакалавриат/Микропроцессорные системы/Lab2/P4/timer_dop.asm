NAME Delay
PUBLIC	Delay
Delay SEGMENT CODE
RSEG Delay
	MOV R3, #068H 
	MOV TH1, #039H
	MOV TL1, #000H				; Устанавливаем время по формуле.
	SETB IE.7					; Разрешаем прерывания
	SETB ET1					; Разрешаем прерывание от Timer 1
	SETB PT1		; Ставим прерыванию от Timer 1 наивысший приоритет
	MOV TMOD, #00010001B		; Timer 1: режим 1 (16-битный таймер)
	SETB TR1						; Запускаем таймер
RET
END 






























































































